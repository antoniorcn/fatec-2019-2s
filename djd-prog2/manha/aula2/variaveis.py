a = -254

print("Variavel a:", a)
print("Variavel a:" + str(a))

b = hex(a)
# Mostra o valor hexadecimal
print("Valor HEX: ", b)

# Mostar o valor absoluto
c = abs(a)
print("Valor de a (absoluto):", c)

# Mostrar o valor absoluto de a em hexadecimal
print("Valor de a em hexadecimal (absoluto):", hex(abs(a)))

# Mostar o valor em Octal
print("Valor de a em Octal:", oct(abs(a)))


